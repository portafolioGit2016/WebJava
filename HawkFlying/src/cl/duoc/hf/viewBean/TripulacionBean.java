package cl.duoc.hf.viewBean;

public class TripulacionBean {

	private Integer idVuelo;
	private Integer idPiloto;
	private Integer tiempoPiloto;
	private Integer tiempoCopiloto;
	public Integer getIdVuelo() {
		return idVuelo;
	}
	public void setIdVuelo(Integer idVuelo) {
		this.idVuelo = idVuelo;
	}
	public Integer getIdPiloto() {
		return idPiloto;
	}
	public void setIdPiloto(Integer idPiloto) {
		this.idPiloto = idPiloto;
	}
	public Integer getTiempoPiloto() {
		return tiempoPiloto;
	}
	public void setTiempoPiloto(Integer tiempoPiloto) {
		this.tiempoPiloto = tiempoPiloto;
	}
	public Integer getTiempoCopiloto() {
		return tiempoCopiloto;
	}
	public void setTiempoCopiloto(Integer tiempoCopiloto) {
		this.tiempoCopiloto = tiempoCopiloto;
	}
	
	

}
