package cl.duoc.hf.viewBean;

public class PilotoBean {
	private String fecha_vencimientomae;
	private String hora_total_vuelo;
	private String id_usuario;
	public String getFecha_vencimientomae() {
		return fecha_vencimientomae;
	}
	public void setFecha_vencimientomae(String fecha_vencimientomae) {
		this.fecha_vencimientomae = fecha_vencimientomae;
	}
	public String getHora_total_vuelo() {
		return hora_total_vuelo;
	}
	public void setHora_total_vuelo(String hora_total_vuelo) {
		this.hora_total_vuelo = hora_total_vuelo;
	}
	public String getId_usuario() {
		return id_usuario;
	}
	public void setId_usuario(String id_usuario) {
		this.id_usuario = id_usuario;
	}
}
